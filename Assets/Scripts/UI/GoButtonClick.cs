    public GameObject HighScorePanel;

    //This script reads the name and sanitizes if required
    private void Start()
    {
        // Get the Button component on this GameObject 
        Button button = GetComponent<Button>();

        if (button != null)
        {
            // Tell Unity: "Hey, when this button gets clicked, run the OnClick() method below"
            button.onClick.AddListener(OnClick);
        }
        else
        {
            Debug.LogWarning("No Button component found on " + gameObject.name);
        }

        HighScoreController = GameObject.Find("Controller");
    }

    private void OnClick()
    {
        // This method runs when the triangle is clicked
        if (nameChars.Count < 5)
        {
            Debug.LogWarning("Some or all of the name chars are missing!");
            return;
        }

        string userName = "";
        char newChar;
        // Get the current letter from the text
        for (int x = 0; x < nameChars.Count; x++)
        {
            //Tenary operator (fancy IF statement) - look it up!
            newChar = nameChars[x].text.Length > 0 ? nameChars[x].text[0] : ' ';
            userName = userName + newChar.ToString();
        }
      


        if (IsThisARudeWord(userName))
        {
            userName = "GIGGLES";
        }

        //Send to highscore
        HighScoreController.GetComponent<HighScore>().AddHighScore(userName);
        HighScorePanel.SetActive(true);        
        HighScoreController.GetComponent<HighScore>().StartUp();
       
        //Turn off the current name entry panel
        GameObject parent = transform.parent.gameObject;
        parent.SetActive(false);



    }



    private bool IsThisARudeWord(string input)
    {
        input = input.Trim(); //Remove any whitespace

        // Check all possible 1-5 character substrings
        for (int length = 1; length <= 5; length++)
        {
            for (int start = 0; start <= input.Length - length; start++)
            {
                string substring = input.Substring(start, length);
                string hash = GetHash(substring);

                if (forbiddenHashes.Contains(hash))
                {
                    return true;
                }
            }
        }

        return false;
    }

    private string GetHash(string input)
    {
        using (SHA256 sha256 = SHA256.Create())
        {
            byte[] bytes = sha256.ComputeHash(Encoding.UTF8.GetBytes(input));
            StringBuilder builder = new StringBuilder();

            for (int i = 0; i < bytes.Length; i++)
            {
                builder.Append(bytes[i].ToString("X2"));
            }

            return builder.ToString();
        }
    }


